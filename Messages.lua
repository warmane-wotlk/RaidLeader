function RL_LoadPaladinBuffData()
  if RaidLeaderData.useKorean then
    return RL_PALADIN_BUFFS_KOR
  else
    return RL_PALADIN_BUFFS_ENG
  end
end

function RL_LoadRaidWarningData()
  if RaidLeaderData.useKorean then
    return RL_MESSAGE_KOR
  else
    return RL_MESSAGE_ENG
  end
end

function RL_LoadRaidWarningTooltipData()
  if RaidLeaderData.useKorean then
    return RL_MESSAGE_TOOLTIP_KOR
  else
    return RL_MESSAGE_TOOLTIP_ENG
  end
end


